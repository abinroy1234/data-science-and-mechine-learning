import numpy as np

A = np.array([[1, 2, 3], [2, 3, 4], [3, 4, 5]])
B = np.array([[1, 1, 1], [2, 2, 2], [3, 3, 3]])

print("Addition \n", np.add(A, B))
print("Subtraction \n", np.subtract(A, B))
print("Multiplication \n", np.multiply(A, B))
print("Division \n", np.divide(A, B))
print("Matrix Multiplication \n", np.matmul(A, B))
print("Traspose A \n", np.transpose(A))
print("Traspose B \n", np.transpose(B))
print("Diagonal sum A \n", np.trace(A))
print("Diagonal sum B \n", np.trace(B))
